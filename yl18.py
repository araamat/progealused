# Väljasta korduslause abil numbrid 5..1 (for in, range)

for x in range(5, 0, -1):
  print(x)
  
  # range (start,stop,step). Stop ei kuulu hulka ja selletõttu tuleb 0 panna.
  
#NÄIDE (for in. Range asemel võib ka list olla.)
mylist = ["apple", "strawberry", "rasberry"]

for fruit in mylist: 
  print(fruit)
print(mylist)
